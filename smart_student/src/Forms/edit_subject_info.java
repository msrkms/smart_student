    public void setInfo(){
        ViewSubjectInfoBackEnd viewsubject = new ViewSubjectInfoBackEnd(CourseCode);
        viewsubject.getInfoFromDataBase();
        CourseCodeTF.setText(CourseCode);
        CourseNameTF.setText(viewsubject.CourseName);
        CourseCreditTF.setText(String.valueOf(viewsubject.CourseCredit));
        TeacherName.setText(viewsubject.CourseTeacher);
        TeacherMailTF.setText(viewsubject.TeacherEmail);
        TeacherPhoneTF.setText(viewsubject.TecherPhone);
        ClassDurationTF.setText(String.valueOf(viewsubject.Duration));
        
    }
