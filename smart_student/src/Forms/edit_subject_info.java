    private void UpdateInfoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateInfoBtnActionPerformed
        CourseCode=CourseCodeTF.getText();
        String CourseName=CourseNameTF.getText();
        int CourseCredit=Integer.parseInt(CourseCreditTF.getText());
        String CourseTeacher=TeacherName.getText();
        String TeacherMail=TeacherMailTF.getText();
        String TeacherPhone=TeacherPhoneTF.getText();
        double ClassDuration=Double.parseDouble(ClassDurationTF.getText()) ;
        EditSubjectInfoBackEnd editsubject = new EditSubjectInfoBackEnd();
        editsubject.setValue(CourseCode, CourseName, CourseCredit, CourseTeacher, TeacherMail, TeacherPhone, ClassDuration);
        boolean Success=editsubject.updateInfo();
        if(Success==true){
            JOptionPane.showMessageDialog(rootPane,"Information Updated");
            new EditClass().setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "No Information Updated");
        }
    }//GEN-LAST:event_UpdateInfoBtnActionPerformed

    
